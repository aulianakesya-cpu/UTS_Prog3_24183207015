    private void cetakStruckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakStruckActionPerformed
        if (modelDaftarPesanan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Belum ada pesanan di daftar!");
            return;
        }

        StringBuilder struk = new StringBuilder("=== STRUK PESANAN ===\n");
        struk.append("Nama: ").append(nama_field.getText()).append("\n");
        struk.append("----------------------------\n");

        for (int i = 0; i < modelDaftarPesanan.getSize(); i++) {
            struk.append(modelDaftarPesanan.getElementAt(i)).append("\n");
        }

        struk.append("----------------------------\n");
        struk.append("Catatan: ").append(catatan_field.getText()).append("\n");
        struk.append("TOTAL BAYAR: Rp ").append(grandTotal);
        

        JOptionPane.showMessageDialog(this, struk.toString(), "Pesanan Berhasil", JOptionPane.INFORMATION_MESSAGE);
    
    // Reset semuanya setelah cetak
        modelDaftarPesanan.clear();
        grandTotal = 0;
        label_grand_total.setText("GRAND TOTAL: Rp 0");
        
        no_meja_field.setText("");      
        nama_field.setText("");         
        catatan_field.setText("");
        harga.setText("");
        jumlah.setValue(0);
        dropdown_kategori.setSelectedIndex(0);
        pembayaran_box.setSelectedIndex(0);
    }//GEN-LAST:event_cetakStruckActionPerformed

    private void btn_hapus_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapus_itemActionPerformed
        int selectedIndex = list_keranjang.getSelectedIndex();

    // Pastikan user benar-benar memilih item (index tidak -1)
    if (selectedIndex != -1) {
        // Ambil teks dari item yang dipilih
        String itemText = modelDaftarPesanan.getElementAt(selectedIndex);

        try {
            // Karena format text Anda sebelumnya: "Menu (x porsi) - Rp 50000"
            // Kita pisahkan (split) string-nya berdasarkan "- Rp " untuk mengambil angkanya saja
            String[] parts = itemText.split("- Rp ");
            
            if (parts.length == 2) {
                // Konversi string harga yang didapat kembali menjadi angka integer
                int hargaItem = Integer.parseInt(parts[1].trim());
                
                // Kurangi grandTotal saat ini dengan harga item yang dihapus
                grandTotal -= hargaItem; 
            }
        } catch (Exception e) {
            System.out.println("Gagal mengekstrak harga dari list");
        }

        // 2. Hapus item tersebut dari list model keranjang
        modelDaftarPesanan.remove(selectedIndex);

        // 3. Jika keranjang kosong setelah dihapus, pastikan grand total jadi 0
        if (modelDaftarPesanan.isEmpty()) {
            grandTotal = 0;
        }

        // 4. Update tulisan di Label Grand Total
        label_grand_total.setText("GRAND TOTAL: Rp " + grandTotal);

    } else {
        // Tampilkan peringatan jika user klik hapus tapi belum memilih item di list
        JOptionPane.showMessageDialog(this, "Pilih pesanan yang ingin dihapus terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btn_hapus_itemActionPerformed

