    private void hitungTotal() {
        try {
        // Hanya menghitung jika textfield harga tidak kosong
            if (!harga.getText().isEmpty()) {
                int hrg = Integer.parseInt(harga.getText());
                int jml = (Integer) jumlah.getValue(); // Mengambil nilai dari JSpinner 'jumlah'
            
            // Mencegah user memasukkan angka minus atau nol di JSpinner
            if (jml < 1) {
                jumlah.setValue(1);
                jml = 1;
                }
            
                int total = hrg * jml;
                total_harga.setText(String.valueOf(total)); // Menampilkan ke textfield 'total_harga'
            }
        } catch (NumberFormatException e) {
            total_harga.setText("0");
        }
        
        list_keranjang.setModel(modelDaftarPesanan);
        
        
    }
